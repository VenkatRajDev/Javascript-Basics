// string inbuilt properties and methods 

let greet = "Hello,"
let sweet = "world."

// length property
console.log(greet.length)

// charAt method 
console.log(greet.charAt(0))

// concat method
console.log(greet.concat(sweet))
console.log('Ve'.concat('nk').concat('at')) // we can define a cocat method this way also

// indexof method
console.log(greet.indexOf("l")) // its return the first position that match a character

//lastIndexOf method
console.log(greet.lastIndexOf("hi")) // its find the last match otherwise its work as the indexof method

// split method
console.log(greet.split(""))
console.log(greet.charAt(2))

// Uppercase method
console.log(sweet.toUpperCase())

// Lowercase method
console.log(sweet.toLowerCase())